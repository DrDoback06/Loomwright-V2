const fs = require("node:fs");
const path = require("node:path");

const names = JSON.parse(
  fs.readFileSync(path.join(__dirname, "callback-names.json"), "utf8"),
);

const out = `// Auto-generated by scripts/generate-callback-names-data.js — do not edit by hand.
window.__LW_CALLBACK_NAMES = ${JSON.stringify(names, null, 2)};
`;

fs.writeFileSync(path.join(__dirname, "..", "callback-names-data.jsx"), out);
console.log("Wrote callback-names-data.jsx with", names.length, "names");
