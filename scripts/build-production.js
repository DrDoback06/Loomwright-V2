#!/usr/bin/env node
/* eslint-disable no-console */
/**
 * scripts/build-production.js
 *
 * Production build for Loomwright v2.
 *
 * The app's source files are not ES modules — every .jsx is loaded by the
 * development shell as its own classic script. Babel Standalone transforms
 * each file independently with `react` + `transform-block-scoping`, so its
 * top-level const/let declarations become shared global vars in script order.
 *
 * Production must preserve that exact boundary. Compiling all raw sources as
 * one lexical program changes the semantics: a later extension script cannot
 * enhance a binding declared as `const` by an earlier file. We therefore
 * precompile EACH ordered source file independently with the identical Babel
 * settings, then concatenate the resulting classic-script output.
 *
 * Output:
 *   dist/index.html              production entry
 *   dist/loomwright.bundle.js    ordered precompiled classic scripts
 *   dist/*.css                   stylesheets (copied)
 *   dist/vendor/react*.js        React + ReactDOM (copied; no babel)
 */

const fs = require("node:fs");
const path = require("node:path");
const Babel = require("@babel/standalone");

const ROOT = path.resolve(__dirname, "..");
const SHELL = path.join(ROOT, "Loomwright Shell.html");
const OUT = path.join(ROOT, "dist");

// Identical to the runtime data-presets / data-plugins in the shell.
const BABEL_OPTIONS = { presets: ["react"], plugins: ["transform-block-scoping"] };

function read(p) { return fs.readFileSync(p, "utf8"); }

// ---- 1. Parse the shell for ordered script + css sources ----
function parseShell(html) {
  const scripts = [];
  const scriptRe = /<script\b[^>]*\bsrc="([^"]+)"[^>]*><\/script>/g;
  let m;
  while ((m = scriptRe.exec(html)) !== null) scripts.push(m[1]);

  const css = [];
  const cssRe = /<link\b[^>]*\brel="stylesheet"[^>]*\bhref="([^"]+)"[^>]*\/?>/g;
  while ((m = cssRe.exec(html)) !== null) css.push(m[1]);
  return { scripts, css };
}

const html = read(SHELL);
const { scripts, css } = parseShell(html);

const VENDOR_PREFIX = "vendor/";
const vendorScripts = scripts.filter((s) => s.startsWith(VENDOR_PREFIX));
const appScripts = scripts.filter((s) => !s.startsWith(VENDOR_PREFIX));

// React + ReactDOM are kept for production; Babel Standalone is dropped.
const reactVendor = vendorScripts.filter((s) => /react/i.test(s));
const droppedBabel = vendorScripts.filter((s) => /babel/i.test(s));

console.log(`Shell defines ${appScripts.length} app scripts, ${css.length} stylesheets.`);

// ---- 2. Compile each classic source script independently ----
// This mirrors the dev shell's one <script type="text/babel" src="…"> per
// source file. The compiled outputs are then joined in the exact same order.
console.log("Compiling ordered app scripts independently with @babel/standalone…");
const compiledParts = [];
for (const rel of appScripts) {
  const filePath = path.join(ROOT, rel);
  if (!fs.existsSync(filePath)) {
    throw new Error(`build-production: shell references missing script "${rel}"`);
  }

  const src = read(filePath);
  let code;
  try {
    code = Babel.transform(src, {
      ...BABEL_OPTIONS,
      filename: rel,
      sourceType: "script",
    }).code;
  } catch (err) {
    console.error(`build-production: Babel transform failed in ${rel}:`, err.message);
    process.exit(1);
  }
  compiledParts.push(`\n// ==== file: ${rel} ====\n${code}\n`);
}
const compiled = compiledParts.join("");

const banner = `/* Loomwright v2 — production bundle. Generated by scripts/build-production.js.\n` +
  `   Do not edit by hand. Source of truth: the .jsx files listed in "Loomwright Shell.html".\n` +
  `   Each source file is independently precompiled as a classic script, then concatenated in shell order.\n` +
  `   No in-browser Babel. Built: ${new Date().toISOString()} */\n`;
const bundle = banner + compiled;

// ---- 3. Write dist ----
fs.rmSync(OUT, { recursive: true, force: true });
fs.mkdirSync(OUT, { recursive: true });
fs.mkdirSync(path.join(OUT, "vendor"), { recursive: true });

fs.writeFileSync(path.join(OUT, "loomwright.bundle.js"), bundle, "utf8");
console.log(`Wrote dist/loomwright.bundle.js (${(bundle.length / 1024).toFixed(0)} KB).`);

// Copy CSS.
for (const href of css) {
  if (/^https?:\/\//.test(href)) continue;
  const srcPath = path.join(ROOT, href);
  if (fs.existsSync(srcPath)) {
    fs.mkdirSync(path.dirname(path.join(OUT, href)), { recursive: true });
    fs.copyFileSync(srcPath, path.join(OUT, href));
  }
}

// Copy React vendor (not Babel).
for (const v of reactVendor) {
  const srcPath = path.join(ROOT, v);
  if (fs.existsSync(srcPath)) fs.copyFileSync(srcPath, path.join(OUT, v));
}

// ---- 4. Generate production index.html ----
const htmlTagMatch = html.match(/<html\b[^>]*>/i);
const htmlTag = htmlTagMatch ? htmlTagMatch[0] : '<html lang="en">';

const prodHtml = `<!doctype html>
${htmlTag}
<head>
  <meta charset="utf-8"/>
  <meta name="viewport" content="width=1280, initial-scale=1"/>
  <title>Loomwright v2</title>
  <link rel="icon" href="data:,"/>

  <link rel="preconnect" href="https://fonts.googleapis.com"/>
  <link rel="preconnect" href="https://fonts.gstatic.com" crossorigin/>
  <link href="https://fonts.googleapis.com/css2?family=Cormorant+Garamond:ital,wght@0,400;0,500;0,600;1,400&family=EB+Garamond:ital,wght@0,400;0,500;0,600;1,400&family=Source+Serif+4:ital,wght@0,400;0,500;0,600;1,400&family=Inter+Tight:wght@400;500;600;700&family=JetBrains+Mono:wght@400;500&display=swap" rel="stylesheet"/>

${css.filter((h) => !/^https?:\/\//.test(h)).map((h) => `  <link rel="stylesheet" href="${h}"/>`).join("\n")}
</head>
<body>
  <div id="root"></div>

  <!-- Production build: ordered precompiled classic scripts, no in-browser Babel. -->
  <script src="vendor/react.development.js"></script>
  <script src="vendor/react-dom.development.js"></script>
  <script src="loomwright.bundle.js"></script>
</body>
</html>
`;
fs.writeFileSync(path.join(OUT, "index.html"), prodHtml, "utf8");
console.log("Wrote dist/index.html (production entry).");

console.log(`Dropped from production path: ${droppedBabel.join(", ") || "(none)"}`);
console.log("Production build complete → dist/");
