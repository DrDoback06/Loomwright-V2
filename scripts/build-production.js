#!/usr/bin/env node
/* eslint-disable no-console */
/**
 * scripts/build-production.js
 *
 * Production build for Loomwright v2.
 *
 * The app's source files are not ES modules — every .jsx declares
 * top-level const/let that, under Babel Standalone's classic-script
 * eval with the `transform-block-scoping` plugin, become global `var`s
 * shared across files by script order. The dev shell
 * (`Loomwright Shell.html`) transforms them in the browser at runtime.
 *
 * This build precompiles them once, in Node, with the IDENTICAL Babel
 * config the runtime uses, concatenated in the exact shell order, into
 * a single classic <script> bundle. The generated `dist/index.html`
 * loads vendored React + the compiled bundle — NO in-browser Babel,
 * NO CDN runtime dependency.
 *
 * Output:
 *   dist/index.html              production entry
 *   dist/loomwright.bundle.js    precompiled, concatenated app
 *   dist/*.css                   stylesheets (copied)
 *   dist/vendor/react*.js        React + ReactDOM (copied; no babel)
 *   dist/<assets>                any other referenced static files
 */

const fs = require("node:fs");
const path = require("node:path");
const Babel = require("@babel/standalone");

const ROOT = path.resolve(__dirname, "..");
const SHELL = path.join(ROOT, "Loomwright Shell.html");
const OUT = path.join(ROOT, "dist");

// Identical to the runtime data-presets / data-plugins in the shell.
const BABEL_OPTIONS = { presets: ["react"], plugins: ["transform-block-scoping"] };

function read(p) { return fs.readFileSync(p, "utf8"); }

// ---- 1. Parse the shell for ordered script + css sources ----
function parseShell(html) {
  const scripts = [];
  const scriptRe = /<script\b[^>]*\bsrc="([^"]+)"[^>]*><\/script>/g;
  let m;
  while ((m = scriptRe.exec(html)) !== null) {
    scripts.push(m[1]);
  }
  const css = [];
  const cssRe = /<link\b[^>]*\brel="stylesheet"[^>]*\bhref="([^"]+)"[^>]*\/?>/g;
  while ((m = cssRe.exec(html)) !== null) {
    css.push(m[1]);
  }
  return { scripts, css };
}

const html = read(SHELL);
const { scripts, css } = parseShell(html);

// Split vendored runtime (react/babel) from app source scripts.
const VENDOR_PREFIX = "vendor/";
const vendorScripts = scripts.filter((s) => s.startsWith(VENDOR_PREFIX));
const appScripts = scripts.filter((s) => !s.startsWith(VENDOR_PREFIX));

// React + ReactDOM are kept for production; Babel Standalone is dropped.
const reactVendor = vendorScripts.filter((s) => /react/i.test(s));
const droppedBabel = vendorScripts.filter((s) => /babel/i.test(s));

console.log(`Shell defines ${appScripts.length} app scripts, ${cssCount(css)} stylesheets.`);
function cssCount(arr) { return arr.length; }

// ---- 2. Concatenate app scripts in shell order ----
let concat = "";
for (const rel of appScripts) {
  const filePath = path.join(ROOT, rel);
  if (!fs.existsSync(filePath)) {
    throw new Error(`build-production: shell references missing script "${rel}"`);
  }
  const src = read(filePath);
  concat += `\n// ==== file: ${rel} ====\n${src}\n`;
}

// ---- 3. Compile once with the runtime Babel config ----
console.log("Compiling concatenated app source with @babel/standalone…");
let compiled;
try {
  compiled = Babel.transform(concat, { ...BABEL_OPTIONS, filename: "loomwright.bundle.jsx", sourceType: "script" }).code;
} catch (err) {
  console.error("build-production: Babel transform failed:", err.message);
  process.exit(1);
}

const banner = `/* Loomwright v2 — production bundle. Generated by scripts/build-production.js.\n` +
  `   Do not edit by hand. Source of truth: the .jsx files listed in "Loomwright Shell.html".\n` +
  `   Precompiled (no in-browser Babel). Built: ${new Date().toISOString()} */\n`;
const bundle = banner + compiled;

// ---- 4. Write dist ----
fs.rmSync(OUT, { recursive: true, force: true });
fs.mkdirSync(OUT, { recursive: true });
fs.mkdirSync(path.join(OUT, "vendor"), { recursive: true });

fs.writeFileSync(path.join(OUT, "loomwright.bundle.js"), bundle, "utf8");
console.log(`Wrote dist/loomwright.bundle.js (${(bundle.length / 1024).toFixed(0)} KB).`);

// Copy CSS.
for (const href of css) {
  if (/^https?:\/\//.test(href)) continue; // skip remote font CSS
  const srcPath = path.join(ROOT, href);
  if (fs.existsSync(srcPath)) {
    fs.mkdirSync(path.dirname(path.join(OUT, href)), { recursive: true });
    fs.copyFileSync(srcPath, path.join(OUT, href));
  }
}

// Copy React vendor (not babel).
for (const v of reactVendor) {
  const srcPath = path.join(ROOT, v);
  if (fs.existsSync(srcPath)) fs.copyFileSync(srcPath, path.join(OUT, v));
}

// ---- PWA assets: manifest + icon + service worker ----
// The SW is a template: substitute a build-stamped cache name and the
// exact asset list so each deploy invalidates the previous cache.
const BUILD_STAMP = new Date().toISOString().replace(/[:.]/g, "-");
const localCss = css.filter((h) => !/^https?:\/\//.test(h));
const swAssets = [
  "./index.html",
  "loomwright.bundle.js",
  "manifest.json",
  "icons/loomwright-icon.svg",
  ...reactVendor,
  ...localCss,
];

fs.copyFileSync(path.join(ROOT, "manifest.json"), path.join(OUT, "manifest.json"));
fs.mkdirSync(path.join(OUT, "icons"), { recursive: true });
fs.copyFileSync(path.join(ROOT, "icons", "loomwright-icon.svg"), path.join(OUT, "icons", "loomwright-icon.svg"));

const swTemplate = read(path.join(ROOT, "sw.js"));
const swCompiled = swTemplate
  .replace("__LW_CACHE_NAME__", `loomwright-${BUILD_STAMP}`)
  .replace("__LW_ASSETS__", JSON.stringify(swAssets, null, 2));
fs.writeFileSync(path.join(OUT, "sw.js"), swCompiled, "utf8");
console.log(`Wrote dist/sw.js (cache loomwright-${BUILD_STAMP}, ${swAssets.length} precached assets) + manifest + icon.`);

// Copy any other static assets referenced by CSS-adjacent files we know
// about (images/fonts live via Google Fonts CDN in <head>, kept as-is).

// ---- 5. Generate production index.html ----
// Reuse the shell's <head> CSS + font links, swap the body to load the
// compiled bundle. Keep the same root element + theme attributes.
const headMatch = html.match(/<head>([\s\S]*?)<\/head>/i);
const headInner = headMatch ? headMatch[1] : "";
const htmlTagMatch = html.match(/<html\b[^>]*>/i);
const htmlTag = htmlTagMatch ? htmlTagMatch[0] : '<html lang="en">';

const prodHtml = `<!doctype html>
${htmlTag}
<head>
  <meta charset="utf-8"/>
  <meta name="viewport" content="width=device-width, initial-scale=1, viewport-fit=cover"/>
  <meta name="theme-color" content="#f4ecd8"/>
  <title>Loomwright v2</title>
  <link rel="icon" href="icons/loomwright-icon.svg" type="image/svg+xml"/>
  <link rel="manifest" href="manifest.json"/>
  <link rel="apple-touch-icon" href="icons/loomwright-icon.svg"/>

  <link rel="preconnect" href="https://fonts.googleapis.com"/>
  <link rel="preconnect" href="https://fonts.gstatic.com" crossorigin/>
  <link href="https://fonts.googleapis.com/css2?family=Cormorant+Garamond:ital,wght@0,400;0,500;0,600;1,400&family=EB+Garamond:ital,wght@0,400;0,500;0,600;1,400&family=Source+Serif+4:ital,wght@0,400;0,500;0,600;1,400&family=Inter+Tight:wght@400;500;600;700&family=JetBrains+Mono:wght@400;500&display=swap" rel="stylesheet"/>

${css.filter((h) => !/^https?:\/\//.test(h)).map((h) => `  <link rel="stylesheet" href="${h}"/>`).join("\n")}
</head>
<body>
  <div id="root"></div>

  <!-- Production build: precompiled bundle, no in-browser Babel, no CDN runtime. -->
  <script src="vendor/react.development.js"></script>
  <script src="vendor/react-dom.development.js"></script>
  <script src="loomwright.bundle.js"></script>
  <script>
    // Offline shell — production only (vite dev serves source directly).
    if ("serviceWorker" in navigator && location.protocol !== "file:") {
      window.addEventListener("load", function () {
        navigator.serviceWorker.register("./sw.js").catch(function () {});
      });
    }
  </script>
</body>
</html>
`;
fs.writeFileSync(path.join(OUT, "index.html"), prodHtml, "utf8");
console.log("Wrote dist/index.html (production entry).");

console.log(`Dropped from production path: ${droppedBabel.join(", ") || "(none)"}`);
console.log("Production build complete → dist/");
